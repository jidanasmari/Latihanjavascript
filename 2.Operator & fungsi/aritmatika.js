let bil1 = 10;
let bil2 = 3;

//operator pejumlahan
let jumlah;
jumlah = bil1 + bil2;
console.log(jumlah)

//operator pengurangan
let kurang;
kurang = bil1 - bil2;
console.log(kurang)

//operator perkalian
let kali;
kali = bil1 * bil2;
console.log(kali)

//operator pembagian
let bagi;
bagi = bil1 / bil2;
console.log(bagi)

//eksponen (pangkat)
let pangkat;
pangkat = bil1 ** bil2;
console.log(pangkat)

//modulus (sisa hasil bagi)
let modulus;
modulus = bil1 % bil2;
console.log(modulus)

//increment | decrement
bil1++;
console.log(bil1);
bil2--;
console.log(bil2)